# Loop through the index numbers 
y = ("durgesh", "durex", "python")
for i in range(len(y)):
    print(y[i])     #Ouput:durgesh
                           # durex
                           # python

# # Using it by while loop
name = ("apple", "banana", "cherry")
i = 0
while i < len(name):
    print(name[i])
    i = i + 1    #Output: apple
                         # banana
                          # cherry