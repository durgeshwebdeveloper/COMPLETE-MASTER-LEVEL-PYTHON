# Joining of two tuples
x = ("a", "b" , "c")
y = (1,2,3)
z = x + y
print(z)    #Output:('a', 'b', 'c', 1, 2, 3)

# Multiply of two tuples
fruits = ("apple", "banana", "cherry")
durgesh = fruits * 2
print(durgesh)     #Ouput:('apple', 'banana', 'cherry', 'apple', 'banana', 'cherry')

